package alone.in.deepspace;


public class Log {
	public static void debug(String str) {
		if (str != null) {
			System.out.println(str);
		}
	}

	public static void error(String str) {
		if (str != null) {
			System.out.println(str);
		}
	}

	public static void info(String str) {
		if (str != null) {
			System.out.println(str);
		}
	}

	public static void warning(String str) {
		if (str != null) {
			System.out.println(str);
		}
	}
}
